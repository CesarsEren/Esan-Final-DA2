    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String buscar = cboBuscar.getSelectedItem().toString();
        switch (buscar) {
            case "TODOS":
                defaultTableModel = clienteDAO.llenartabla();
                break;
            case "DNI":
                defaultTableModel = clienteDAO.buscarEnTabla("nif", txtBuscador.getText());
                break;
            case "NOMBRE":
                defaultTableModel = clienteDAO.buscarEnTabla("nombre", txtBuscador.getText());
                break;
            case "TELEFONO":
                defaultTableModel = clienteDAO.buscarEnTabla("telefono", txtBuscador.getText());
                break;
            case "CELULAR":
                defaultTableModel = clienteDAO.buscarEnTabla("celular", txtBuscador.getText());
                break;
            default:
                throw new AssertionError();
        }
        tblCliente.setModel(defaultTableModel);
    }//GEN-LAST:event_jButton1ActionPerformed


