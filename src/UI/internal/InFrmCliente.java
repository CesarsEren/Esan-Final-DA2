    private void tblClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClienteMouseClicked

        int selectedRow = tblCliente.getSelectedRow();
        if (selectedRow != -1) {
            // Obtener datos de la fila seleccionada
            int idCliente = Integer.parseInt((String) tblCliente.getValueAt(selectedRow, 0));
            //  String[] dt = ((String) jtblTrabajador.getValueAt(selectedRow, 1)).split("\\|");
            //ComboData puesto = new ComboData(Integer.parseInt(dt[0].toString().trim()), dt[1].toString().trim());
            String docIdent = (String) tblCliente.getValueAt(selectedRow, 1);
            String nombre = (String) tblCliente.getValueAt(selectedRow, 2);
            String ubigeo = (String) tblCliente.getValueAt(selectedRow, 3);

            //idCliente,nif,nombre,idUbigeo,direccion,celular,telefono,correo,estado
            Object[][] ubigeos = ubigeoDAO.getRowByIdUbigeo(ubigeo);
            cboDepa.setSelectedItem(ubigeos[0][1].toString());
            cboProv.setSelectedItem(ubigeos[0][2].toString());
            cboDistrito.setSelectedItem(ubigeos[0][3].toString());
            String direccion = (String) tblCliente.getValueAt(selectedRow, 4);
            String celular = (String) tblCliente.getValueAt(selectedRow, 5);
            String telefono = (String) tblCliente.getValueAt(selectedRow, 6);
            String correo = (String) tblCliente.getValueAt(selectedRow, 7);
            short estado = (short) (("Activo".equals(tblCliente.getValueAt(selectedRow, 8).toString())) ? 1 : 0);

            Cliente cliente = new Cliente(idCliente, docIdent, nombre, ubigeo, direccion, celular, telefono, correo, estado);
            setDataCliente(cliente);
            btnregistro.setText("Actualizar");
        }
    }//GEN-LAST:event_tblClienteMouseClicked

