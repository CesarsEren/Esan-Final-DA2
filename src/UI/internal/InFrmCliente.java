    private void btnregistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistroActionPerformed
        // TODO add your handling code here:

        Cliente t = new Cliente();
        t.setDireccion(txtdireccion.getText());
        t.setNombre(txtnombre.getText());
        t.setCelular(txtCelular.getText());
        t.setCorreo(txtCorreo.getText());
        t.setIdUbigeo(txtUbigeo.getText());
        t.setNif(txtnroDocumento.getText());
        t.setTelefono(txtTelefono.getText());
        t.setEstado((short) (rbActivo.isSelected() ? 1 : 0));
        boolean condicionerror
                = llenarError(
                        txtdireccion,
                        txtnombre,
                        txtCelular,
                        txtCorreo,
                        txtUbigeo,
                        txtnroDocumento,
                        txtTelefono);

        System.out.println(this.btnregistro.getText());
        if (!condicionerror) {
            switch (this.btnregistro.getText()) {
                case "Registrar":
                    t.setIdCliente(0);
                    if (clienteDAO.save(t)) {
                        JOptionPane.showMessageDialog(this, "Cliente Guardado Correctamente", "Mensaje de confirmación", JOptionPane.INFORMATION_MESSAGE);
                        tblCliente.setModel(clienteDAO.llenartabla());
                    }
                    break;
                case "Actualizar":
                    t.setIdCliente(Integer.parseInt(txtid.getText()));
                    if (clienteDAO.save(t)) {
                        JOptionPane.showMessageDialog(this, "Cliente Actualizado Correctamete", "Mensaje de confirmación", JOptionPane.INFORMATION_MESSAGE);
                        tblCliente.setModel(clienteDAO.llenartabla());
                    }
                    break;
            }
        }
    }//GEN-LAST:event_btnregistroActionPerformed

    public boolean llenarError(JTextField... jTxtFields) {
        JPanel panel = new JPanel();
        StringJoiner joiner = new StringJoiner("<br>");
        for (JTextField jtxtfield : jTxtFields) {
            System.out.println("field" + jtxtfield.getName());
            if (jtxtfield.getText().isEmpty()) {
                joiner.add(jtxtfield.getName().concat(" no puede estar vacío"));
            }
        }
        if (!joiner.toString().isEmpty()) {
            panel.add(new JLabel("<html><font color='red'>" + joiner.toString() + "</font></html>"));
            JOptionPane.showOptionDialog(
                    null,
                    panel,
                    "Alerta",
                    JOptionPane.WARNING_MESSAGE,
                    JOptionPane.WARNING_MESSAGE,
                    null,
                    new Object[]{},
                    null
            );
        }
        return !joiner.toString().isEmpty();
    }
