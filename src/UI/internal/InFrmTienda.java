    private static String getSelectedButtonValue(ButtonGroup buttonGroup) {
        for (Enumeration<AbstractButton> buttons = buttonGroup.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                return button.getText();
            }
        }

        return null;
    }

    private void btnregistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistroActionPerformed
        // TODO add your handling code here:
        Tienda t = new Tienda();
        t.setTienda(txtNameTienda.getText());
        t.setIdEstado((short) (rbActivo.isSelected() ? 1 : 0));
        t.setDescr(txtDescripcion.getText());
        t.setIdUbigeo(txtUbigeo.getText());

        System.out.println(this.btnregistro.getText());
        switch (this.btnregistro.getText()) {
            case "Registrar":
                t.setIdTienda(0);
                if (tiendaDAO.save(t)) {
                    JOptionPane.showMessageDialog(this, "Tienda Guardado Correctamete", "Mensaje de confirmación", JOptionPane.INFORMATION_MESSAGE);
                    jbtlTienda.setModel(tiendaDAO.llenartabla());
                }
                break;
            case "Actualizar":
                t.setIdTienda(Integer.parseInt(txtid.getText()));
                if (tiendaDAO.save(t)) {
                    JOptionPane.showMessageDialog(this, "Tienda Actualizada Correctamete", "Mensaje de confirmación", JOptionPane.INFORMATION_MESSAGE);
                    jbtlTienda.setModel(tiendaDAO.llenartabla());
                }
                break;
        }

    }//GEN-LAST:event_btnregistroActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: 
        String buscar = cboBuscar.getSelectedItem().toString();
        switch (buscar) {
            case "TODOS":
                defaultTableModel = tiendaDAO.llenartabla();
                break;
            case "NOMBRE":
                defaultTableModel = tiendaDAO.buscarEnTabla("Tienda", txtBuscador.getText());
                break;
            case "UBIGEO":
                defaultTableModel = tiendaDAO.buscarEnTabla("idUbigeo", txtBuscador.getText());
                break;
            case "DESCRIPCION":
                defaultTableModel = tiendaDAO.buscarEnTabla("descr", txtBuscador.getText());
                break;
            default:
                throw new AssertionError();
        }
        jbtlTienda.setModel(defaultTableModel);
    }//GEN-LAST:event_jButton1ActionPerformed

