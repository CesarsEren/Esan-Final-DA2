    private static int findIndex(DefaultComboBoxModel<ComboData> model, ComboData value) {
        for (int i = 0; i < model.getSize(); i++) {
            if (model.getElementAt(i).getId() == (value.getId())) {
                return i;
            }
        }
        return -1;
    }

    private void jbtlTiendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtlTiendaMouseClicked

        int selectedRow = jbtlTienda.getSelectedRow();

        if (selectedRow != -1) {
            // Obtener datos de la fila seleccionada
            int idTienda = Integer.parseInt((String) jbtlTienda.getValueAt(selectedRow, 0));
            String nombre = (String) jbtlTienda.getValueAt(selectedRow, 1);
            String idUbigeo = jbtlTienda.getValueAt(selectedRow, 2).toString().trim();
            String descripcion = (String) jbtlTienda.getValueAt(selectedRow, 3);
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            short estado = (short) (("Activo".equals(jbtlTienda.getValueAt(selectedRow, 4).toString())) ? 1 : 0);
            Tienda t = new Tienda(idTienda, nombre, idUbigeo, descripcion, estado);
            btnregistro.setText("Actualizar");
            setDataTienda(t);
        }

    }//GEN-LAST:event_jbtlTiendaMouseClicked

